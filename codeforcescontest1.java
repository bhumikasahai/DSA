import java.util.*;
public class codeforcescontest1 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while(t-->0){
           long x = sc.nextLong();
           if(x==67){
            System.out.println(x);
           }else{
            System.out.println(x+1);
           }
        }
    }    
}
